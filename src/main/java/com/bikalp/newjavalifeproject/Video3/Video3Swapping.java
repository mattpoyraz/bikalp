package com.bikalp.newjavalifeproject;



public class Video3Swapping //class
{
    public static void main( String[] args ) //main method
    {
      int a=10;
      int b=20;

      System.out.println("valua of a is before swapping: "+a);
      System.out.println("valua of b is before swapping: "+b);

      int c;

      c=a;
      a=b;
      b=c;
      

      System.out.println("valua of a is after swapping: "+a);
      System.out.println("valua of b is after swapping: "+b);
        
    }
}
